    private void Resize(JLabel lbl){
        jPanel2.addMouseMotionListener(new MouseAdapter() {
            public void mouseDragged(MouseEvent evt2){
                newLinea.setSize(evt2.getX()-newLinea.getX(), newLinea.getHeight());
                System.out.println(lbl.getBounds());
            }
        });
    }
    
    private void CrearDuplicados(JLabel lbl, JLabel refe, String direccion){
        lbl.setIcon(new ImageIcon(direccion));
        jPanel2.add(lbl);
        lbl.setBounds(100, 100, refe.getWidth(), refe.getHeight());
        ArrastraMouse(lbl);
        Resize(lbl);
    }
    
    /**
     * Se crea un metodo general para mover las compuertas al arrastrar el mouse sobre ellas
     * 
     */
    private void ArrastraMouse(JLabel lbl){
        lbl.addMouseMotionListener(new MouseMotionAdapter() {
            @Override
            public void mouseDragged(MouseEvent evt){
                lbl.setLocation(lbl.getX()+evt.getX()-lbl.getWidth()/2, lbl.getY()+evt.getY()-lbl.getHeight()/2);
            }
        });
    }
    
    
